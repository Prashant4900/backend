class Encryption {
  /// Encrypts the [plainText] using AES with the provided [keyStr].
  static String encrypt({
    required String keyStr,
    required String plainText,
  }) {
    return plainText;
  }

  /// Decrypts the [encryptedText] using AES with the provided [keyStr].
  static String decrypt({
    required String keyStr,
    required String encryptedText,
  }) {
    return encryptedText;
  }
}
